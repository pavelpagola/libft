#include "libft.h"

int	ft_atoi(const char *str)
{
	int		sign;
	long	num;

	sign = -1;
	num = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str++ == '-')
			sign = 1;
	}
	while (*str >= '0' && *str <= '9')
		num = num * 10 - (*str++ - '0');
	return ((int)(num * sign));
}
